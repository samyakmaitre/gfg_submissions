class Solution {
    public Node addOne(Node head) {
        //reverse linked list
        head = reverseList(head);
        
        Node curr = head;
        Node prev = null;
        int carry = 1;
        
        //traverse the rev list and add 1
        while(curr!=null){
            int sum = curr.data + carry;
            carry = sum / 10;
            curr.data = sum%10;
            prev =curr;
            curr = curr.next;
        }
        
        if(carry>0){
            prev.next = new Node(carry);
        }
        
        head = reverseList(head);
        
        return head;
    }
    
    private Node reverseList(Node head){
        Node prev = null;
        Node curr = head;
        Node next = null;
        
        while(curr!=null){
            next = curr.next;
            curr.next = prev;
            prev = curr;
            curr =next;
        }
        
        return prev;
    }
}
